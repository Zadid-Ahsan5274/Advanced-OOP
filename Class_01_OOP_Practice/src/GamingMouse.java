
public class GamingMouse implements IMouse{

	@Override
	public void rightClick() {
		System.out.println("Gaming Mouse - right click");
		
	}

	@Override
	public void leftClick() {
		System.out.println("Gaming Mouse - left click");
		
	}

	@Override
	public void singleClick() {
		System.out.println("Gaming Mouse - single click");
		
	}

	@Override
	public void doubleClick() {
		System.out.println("Gaming Mouse - double click");
		
	}

}
